# 리스트
# : 서로 다른 자료형의 값을 저장할 수 있는 컬렉션

# li = [값, 값2, ...]
li = [100, 12.34, 'hello']
print('li : ', li)


# 리스트 인덱싱
print('li[0] : ', li[0])
print('li[1] : ', li[1])
print('li[2] : ', li[2])


# 리스트의 슬라이싱
li2 = ['월','화','수','목','금','토','일']
print('li2[0:5] : ',li2[0:5])
print('li2[:5] : ',li2[:5])

print('li2[5:7] : ',li2[5:7])
print('li2[5:] : ',li2[5:])
